# include <stdio.h>

int main()
{
    // Creating variables with different data types
    int myNum = 10;             // Integer (whole number)
    float myFloatNum = 10.09;   // Floating Point (decimal number)
    char myChar = 'A';          // Character

    // Printing variables
    printf("%d\n", myNum);
    printf("%f\n", myFloatNum);
    printf("%c\n", myChar);

    return 0;
}