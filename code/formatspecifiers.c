# include <stdio.h>

int main()
{
    // Format specifiers
    int i = 0;
    float f = 0.0;
    char c = 'a';
    char str[100] = "Hello World!";

    // Printing them
    printf("Integer: %d\n", i);
    printf("Float: %f\n", f);
    printf("Character: %c\n", c);
    printf("String: %s\n", str);
    getchar();
    return 0;
}
